//Method With Argument and No Return Type 
//Try This...

class Example8a
{
	public static void m1(int a)
	{
		System.out.println("integer");
	}
	public static void m2(double d)
	{
		System.out.println("double");
	}
	public static void m3(char ch)
	{
		System.out.println("char");
	}
	public static void m4(String s)
	{
		System.out.println("String");
	}
	public static void m5(boolean b)
	{
		System.out.println("boolean");
	}
	public static void main(String[] args) 
	{
		System.out.println(m1(10));
		System.out.println(m2(10.5));
		System.out.println(m3('B'));
		System.out.println(m4("ABCD"));
		System.out.println(m5(false));
		
	}
}
